# variable is a name given to a memory location in a program
# datatypes : defines the type of data being stored in the variable

# Rules for defining a variable :-

# Variable names should be meaningful and start with a letter or an underscore
# Variable names should not contain spaces or special characters
# Variable names should not be a reserved keyword in Python
# Variable names should be consistent throughout the code
# Variables cannot start with number digits
# Variable names are case sensitive


# Assigning values to variables
x = 5 # x is an integer

y = 7.5 # y is a float

z = "Hello, World!" # z is a string

a = True # a is a boolean

b = None # b is a None type

c = [1, 2, 3] # c is a list

d = (1, 2, 3) # d is a tuple

e = {1, 2, 3} # e is a set

f = {"x": 1, "y": 2, "z": 3} # f is a dictionary

# Printing the values of variables

print(x)
print(y)
print(z)
print(a)
print(b)
